export class Names {
    names: string;
    namesShort: string;
    contactName: string;
    contactPhone: string;
    contactEmail: string;

    constructor(names: string, namesShort: string, contactName: string, contactPhone: string, contactEmail: string) {
        this.names = names;
        this.namesShort = namesShort;
        this.contactName = contactName;
        this.contactPhone = contactPhone;
        this.contactEmail = contactEmail;
    }
}