export class Names {
    names: string;
    namesShort: string;
    contactName: string;
    contactPhone: string;

    constructor(names: string, namesShort: string, contactName: string, contactPhone: string) {
        this.names = names;
        this.namesShort = namesShort;
        this.contactName = contactName;
        this.contactPhone = contactPhone;
    }
}